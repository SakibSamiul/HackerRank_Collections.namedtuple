
from collections import namedtuple

def avarage():

    totalmarks = 0
    for _ in range(N):
        Student = namedtuple('Student', spreadsheets)

        ID, MARKS, NAME, CLASS = input().split()
        students = (Student(ID, MARKS, NAME, CLASS))
        totalmarks += int(students.MARKS)

    avarage = totalmarks / N
    print(avarage)

if __name__ == '__main__':
    N = int(input())
    spreadsheets = input().split()
    avarage()
